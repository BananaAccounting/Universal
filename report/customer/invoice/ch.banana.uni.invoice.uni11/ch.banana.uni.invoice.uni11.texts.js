function setInvoiceTexts(language) {
  
  /*
    Defines all the texts translations for all the different languages.
  */

  var texts = {};
  
  if (language === 'it') {
    setTexts_it(texts);
  }
  else if (language === 'de') {
    setTexts_de(texts);
  }
  else if (language === 'fr') {
    setTexts_fr(texts);
  }
  else if (language === 'nl') {
    setTexts_nl(texts);
  }
  else if (language === 'zh') {
    setTexts_zh(texts);
  }
  else {
    setTexts_en(texts);
  }

  return texts;
}

